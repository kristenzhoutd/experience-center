// ── Service interface ────────────────────────────────────────────────────────

export interface DAMServiceInterface {
  getProviders(): Promise<DAMProvider[]>;
  getCollections(providerId?: string): Promise<string[]>;
  searchAssets(query: string, providerId?: string, filters?: DAMFilter): Promise<DAMAsset[]>;
  getAssetById(id: string): Promise<DAMAsset | null>;
}

// ── Mock implementation ──────────────────────────────────────────────────────

function delay(ms: number): Promise<void> {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function randomDelay(): Promise<void> {
  return delay(300 + Math.random() * 300);
}

export const damService: DAMServiceInterface = {
  async getProviders(): Promise<DAMProvider[]> {
    await randomDelay();
    return PROVIDERS;
  },

  async getCollections(providerId?: string): Promise<string[]> {
    await randomDelay();
    const assets = providerId
      ? DEMO_ASSETS.filter((a) => a.provider === providerId)
      : DEMO_ASSETS;
    return [...new Set(assets.map((a) => a.collection))].sort();
  },

  async searchAssets(
    query: string,
    providerId?: string,
    filters?: DAMFilter,
  ): Promise<DAMAsset[]> {
    await randomDelay();

    let results = [...DEMO_ASSETS];

    // Filter by provider
    if (providerId) {
      results = results.filter((a) => a.provider === providerId);
    }

    // Text search — matches name, description, tags
    if (query.trim()) {
      const q = query.toLowerCase().trim();
      results = results.filter(
        (a) =>
          a.name.toLowerCase().includes(q) ||
          a.description.toLowerCase().includes(q) ||
          a.tags.some((t) => t.toLowerCase().includes(q)),
      );
    }

    // Apply filters
    if (filters) {
      if (filters.type) {
        results = results.filter((a) => a.type === filters.type);
      }
      if (filters.aspectRatio) {
        results = results.filter((a) => a.aspectRatio === filters.aspectRatio);
      }
      if (filters.usageRights) {
        results = results.filter((a) => a.usageRights === filters.usageRights);
      }
      if (filters.collection) {
        results = results.filter((a) => a.collection === filters.collection);
      }
    }

    return results;
  },

  async getAssetById(id: string): Promise<DAMAsset | null> {
    await delay(200);
    return DEMO_ASSETS.find((a) => a.id === id) ?? null;
  },
};
